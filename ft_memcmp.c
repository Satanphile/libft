int		ft_memcmp(const void *s1, const void *s2, size_t n)
{
	int		i;

	i = 0;
	while ((s1[i] != n) && (s2[i] != n))
	{
		if ((s1[i] == '\0') && (s2[i] == '\0'))
			return (0);
		if ((s1[i] == '\0') && (s1 != s2))
			return (-1);
		if ((s2[i] == '\0') && (s1 != s2))
			return (1);
	}
}
