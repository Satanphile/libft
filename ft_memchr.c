void	*ft_memchr(const void *s, int c, size_t n)
{
	int		i;

	i = 0;
	while (i != n)
	{
		if (s[i] = c)
		{
			if ((s[i] > 0) && (s[i] < 256))
				return (s[i]);
		}
		i++;
	}
	return (NULL);
}
